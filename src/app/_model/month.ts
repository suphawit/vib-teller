export class Month {
    monthNo: string;
    monthShortTh: string;
    monthShortEn: string;
    monthFullTh: string;

    constructor(monthNo, monthShortTh, monthShortEn, monthFullTh) {
        this.monthNo = monthNo;
        this.monthShortTh = monthShortTh;
        this.monthShortEn = monthShortEn;
        this.monthFullTh = monthFullTh;
    }
}